void bitmap_xbm_to_screen_frame(uint8_t* dst, const uint8_t* src, size_t w, size_t h) {
    for(size_t y = 0; y < h; ++y) {
        for(size_t x = 0; x < w; ++x) {
            const size_t ii = (y * w + x) / 8;
            const size_t oi = y / 8 * w + x;

            const size_t iz = x % 8;
            const size_t oz = y % 8;

            if(src[ii] & (1 << iz)) {
                dst[oi] = dst[oi] | (1 << oz);
            }
        }
    }
}
